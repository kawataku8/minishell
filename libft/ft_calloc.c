#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*p;

	if (count == 0 || size == 0)
	{
		count = 1;
		size = 1;
	}
	if (!(p = (void *)malloc(count * size)))
		return (NULL);
	ft_bzero(p, count * size);
	return (p);
}
